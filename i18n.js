let currentLang = localStorage.getItem("ps_lang") || (navigator.language || "tr").slice(0, 2);
if (!I18N[currentLang]) currentLang = "tr";

function applyI18n(lang) {
  if (!I18N[lang]) lang = "tr";
  currentLang = lang;
  localStorage.setItem("ps_lang", lang);
  document.documentElement.lang = lang;
  const dict = I18N[lang];

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    const key = el.getAttribute("data-i18n");
    if (key && dict[key] !== undefined) {
      if (typeof dict[key] === "function") return;
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll(".lang-switch button").forEach(function (b) {
    b.classList.toggle("active", b.dataset.lang === lang);
  });
}

function t(key) {
  const dict = I18N[currentLang] || I18N.tr;
  const val = dict[key];
  if (typeof val === "function") return val(arguments[1]);
  return val !== undefined ? val : key;
}

document.querySelectorAll(".lang-switch button").forEach(function (b) {
  b.addEventListener("click", function () { applyI18n(b.dataset.lang); });
});

applyI18n(currentLang);
